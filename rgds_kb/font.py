def measure_text(text, scale):
    """Return pixel width of a text string at the given scale.

    Each character is 5×scale wide, plus scale pixels of spacing between chars.
    """
    if not text:
        return 0
    char_width = 5 * scale
    spacing = scale
    return len(text) * (char_width + spacing) - spacing


def get_glyph(char):
    """Look up a glyph by character.

    For lowercase letters, returns the lowercase glyph directly.
    For characters not found, tries uppercase fallback.

    Returns tuple of 7 ints, or None if no glyph exists.
    """
    glyph = FONT.get(char)
    if glyph:
        return glyph
    if char.upper() in FONT:
        return FONT[char.upper()]
    return None
